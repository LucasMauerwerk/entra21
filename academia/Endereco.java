package academia;


	public class Endereco {
		String rua;
		int num;
		
		public String getRua() {
			return rua;
		}
		public void setRua(String rua) {
			this.rua = rua;
		}
		public int getNum() {
			return num;
		}
		public void setNum(int num) {
			this.num = num;
		}
		public Endereco(String rua, int num) {
			
			this.rua = rua;
			this.num = num;
		}
		
		
		
		
		
	}


